package com.pcs.tim.myapplication;

public class PartialViewHealthStatus {
    private String handicap;
    private String bloodGroup;
    private String medicalHistory;
    private short bHiv;
    private short bHepatitis ;
    private short bMentalDisorder ;
    private short bCovidTest ;
    private String covidTestDate ;

    public String getHandicap() {
        return handicap;
    }

    public void setHandicap(String handicap) {
        this.handicap = handicap;
    }

    public String getBloodGroup() {
        return bloodGroup;
    }

    public void setBloodGroup(String bloodGroup) {
        this.bloodGroup = bloodGroup;
    }

    public String getMedicalHistory() {
        return medicalHistory;
    }

    public void setMedicalHistory(String medicalHistory) {
        this.medicalHistory = medicalHistory;
    }

    public short getbHiv() {
        return bHiv;
    }

    public void setbHiv(short bHiv) {
        this.bHiv = bHiv;
    }

    public short getbHepatitis() {
        return bHepatitis;
    }

    public void setbHepatitis(short bHepatitis) {
        this.bHepatitis = bHepatitis;
    }

    public short getbMentalDisorder() {
        return bMentalDisorder;
    }

    public void setbMentalDisorder(short bMentalDisorder) {
        this.bMentalDisorder = bMentalDisorder;
    }

    public short getbCovidTest() {
        return bCovidTest;
    }

    public void setbCovidTest(short bCovidTest) {
        this.bCovidTest = bCovidTest;
    }

    public String getCovidTestDate() {
        return covidTestDate;
    }

    public void setCovidTestDate(String covidTestDate) {
        this.covidTestDate = covidTestDate;
    }
}
